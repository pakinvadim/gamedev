import Foundation

func CcpAdd(v1:CGPoint, v2:CGPoint) -> CGPoint
{
    return CGPointMake(v1.x + v2.x, v1.y + v2.y);
}

func Ccp(v1:CGPoint, v2:CGPoint) -> CGPoint
{
    return CGPointMake(v1.x + v2.x, v1.y + v2.y);
}